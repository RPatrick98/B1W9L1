

function addition(x,y) {
	return x + y;
	 
}
document.write("10 + 12 =" + addition(10,12) + "<br>");
document.write("46 + 52 =" + addition(46,52) + "<br>");
document.write("99 + 51 =" + addition(99,51) + "<br>");


function subtraction(x,y) {
	return x - y;
	 
}
document.write("58 - 34 =" + subtraction(58,34) + "<br>");
document.write("1000-100 =" + subtraction(1000,10) + "<br>");
document.write("9500 - 2000 =" + subtraction(9500,2000) + "<br>");


function multiplication(x,y) {
	return x * y;	 

}
document.write("6 * 7 =" + multiplication(6,7) + "<br>");
document.write("12 * 12 =" + multiplication(12,12) + "<br>");
document.write("300 * 300 =" + multiplication(300,300) + "<br>");


function division(x,y) {
	return x / y;
	 
}
document.write("144 : 12 =" + division(144,12) + "<br>");
document.write("3000 : 30 =" + division(3000,30) + "<br>");
document.write("2500 : 500 =" + division(2500,500) + "<br>");


function increment(x) {
	return x + 1;
	 
}
document.write("12 + 1 =" + increment(12) + "<br>");
document.write("2000 + 1 =" + increment(2000) + "<br>");
document.write("250 + 1 =" + increment(250) + "<br>");


function decrement(x) {
	return x - 1;
	 
}
document.write("34 - 1 =" + decrement(12) + "<br>");
document.write("10000 - 1 =" + decrement(10000) + "<br>");
document.write("270 - 1 =" + decrement(270) + "<br>");
